    private void btnActualizarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarPActionPerformed
        String nombre = jTextFieldNombre.getText();
        String telefono = jTextFieldTelefono.getText();
        String codigo = jTextFieldCodigo.getText();

        if (nombre.isEmpty()){
            JOptionPane.showMessageDialog(null, "Debe rellenar el campo de nombre");
        }else if (telefono.isEmpty()){
            
            JOptionPane.showMessageDialog(null, "Debe rellenar el campo de teléfono");
        }else if (!Functions.isNumeric(telefono)){
            JOptionPane.showMessageDialog(null, "El campo teléfono debe ser un campo numérico ");
        }else{
            
             try {

                Connection conexion = this.Conexion();
                PreparedStatement consulta = conexion.prepareStatement("UPDATE empleados SET nombre=?,telefono=? WHERE Codigo_empleado=?");

                long telefonoNumero = Long.parseLong(telefono);
                long codigoNumero = Long.parseLong(codigo);
                
                consulta.setString(1, nombre);
                consulta.setLong(2, telefonoNumero);
                consulta.setLong(3, codigoNumero);
                consulta.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Se han actualizado los datos del empleado correctamente");
                
                jComboBoxListadoEmpleados.addItem(jTextFieldNombre.getText());
                jComboBoxListadoEmpleados.removeItemAt(jComboBoxListadoEmpleados.getSelectedIndex() );
                jComboBoxListadoEmpleados.setSelectedIndex(0);
                

                jTextFieldCodigo.setText("");
                jTextFieldNombre.setText("");
                jTextFieldTelefono.setText("");
      
            } catch (SQLException sqle) {
                sqle.getMessage();
            }
        
        }
    }//GEN-LAST:event_btnActualizarPActionPerformed

