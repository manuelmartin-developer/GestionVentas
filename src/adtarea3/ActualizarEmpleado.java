    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        try {

            Connection conexion = this.Conexion();

            Statement consulta = conexion.createStatement();

            consulta.executeUpdate("UPDATE empleados SET nombre=\'"+jTextFieldNombre.getText() +
                "\', telefono=\'" + jTextFieldTelefono.getText() +
                "\' WHERE Codigo_empleado=\'" + jTextFieldCodigo.getText()+"\'");

            JOptionPane.showMessageDialog(null, "Se han actualizado los datos del empleado correctamente");

            // Actualizamos el listado de empleados en el ComboBox y reiniciamos los campos

            jComboBoxListadoEmpleados.addItem(jTextFieldNombre.getText());
            jComboBoxListadoEmpleados.removeItemAt(jComboBoxListadoEmpleados.getSelectedIndex() );              
            jComboBoxListadoEmpleados.setSelectedIndex(0);

            jTextFieldCodigo.setText("");
            jTextFieldNombre.setText("");
            jTextFieldTelefono.setText("");

        } catch (SQLException sqle) {
            sqle.getMessage();
        }

    }//GEN-LAST:event_btnActualizarActionPerformed

    private void jComboBoxListadoEmpleadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxListadoEmpleadosActionPerformed

        try {

            Connection conexion = this.Conexion();

            Statement consulta = conexion.createStatement();

            ResultSet resultado = consulta.executeQuery("SELECT * FROM empleados WHERE nombre = \'"+jComboBoxListadoEmpleados.getSelectedItem().toString()+"\'");

            while (resultado.next()){

                int codigo = resultado.getInt("Codigo_empleado");
                String nombre = resultado.getString("nombre");
                long telefono = resultado.getLong("telefono");

                //Asignamos los resultados a los JTexts
                jTextFieldCodigo.setText(Integer.toString(codigo));
                jTextFieldNombre.setText(nombre);
                jTextFieldTelefono.setText(String.valueOf(telefono));
            }

        } catch (SQLException sqle) {
            sqle.getMessage();
        }

    }//GEN-LAST:event_jComboBoxListadoEmpleadosActionPerformed

