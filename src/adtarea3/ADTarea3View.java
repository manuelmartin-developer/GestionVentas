    private void btnCrearTablaEmpleadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearTablaEmpleadosActionPerformed
        try {
            if (!existeEmpleados){
                Connection conexion = this.Conexion();
            
            Statement stmt = conexion.createStatement();
            stmt.execute("CREATE TABLE empleados (Codigo_empleado COUNTER PRIMARY KEY, nombre TEXT(255), telefono LONG)");
            JOptionPane.showMessageDialog(null, "Se ha creado la tabla EMPLEADOS de forma existosa");
            btnCrearTablaEmpleados.setEnabled(false);
            btnActualizarEmpleado.setEnabled(true);
            btnInsertarEmpleado.setEnabled(true);
            btnMostrarEmpleados.setEnabled(true);
            }else {
                JOptionPane.showMessageDialog(null, "La tabla Empleados ya ha sido creada anteriormente");
                btnCrearTablaEmpleados.setEnabled(false);
            }
     
        } catch (SQLException sqle) {
            sqle.getMessage();
        }
    }//GEN-LAST:event_btnCrearTablaEmpleadosActionPerformed

