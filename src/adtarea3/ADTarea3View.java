    private Connection Conexion(){
        
        Connection conexion = null;
        // Conexión a la base de datos de la tienda de ropa
        String connectionUrl= "jdbc:ucanaccess://./src\\BBDD\\tiendaropa.mdb" ;
        
        try {
            conexion = DriverManager.getConnection(connectionUrl);
        } catch (SQLException sqle) {
            sqle.getMessage();
        }
        
        return conexion;
        
    }
    
  

    
    //Métodos que comprueban si exiten las tablas. Este método nos servirá también
    // para deshabilitar los botones en caso de que no estén creadas. Nos 
    
    public void existeTablaEmpleados(){
        
        String empleados = "empleados";
        try {
            Connection conexion = this.Conexion();
            Statement stmt = conexion.createStatement();
            
            DatabaseMetaData datos = this.Conexion().getMetaData();
            ResultSet rs = datos.getTables(null, null, "%", null);
            
            while (rs.next()){
                String tabla = rs.getString(3);

                if (empleados.equals(tabla)){
                    existeEmpleados = true;
                }
            }
            
            if (!existeEmpleados){
                JOptionPane.showMessageDialog(null, "Debe crear la tabla empleados");
                btnActualizarEmpleado.setEnabled(false);
                btnInsertarEmpleado.setEnabled(false);
                btnMostrarEmpleados.setEnabled(false); 
            }
            
            
        } catch (SQLException slqe) {
            slqe.getMessage();
        }
        
    }
    
    public void existeTablaVentas(){
         String ventas = "ventas";
        try {
            Connection conexion = this.Conexion();
            Statement stmt = conexion.createStatement();
            
            DatabaseMetaData datos = this.Conexion().getMetaData();
            ResultSet rs = datos.getTables(null, null, "%", null);
            
            while (rs.next()){
                String tabla = rs.getString(3);

                if (ventas.equals(tabla)){
                    existeVentas = true;
                }
            }
            
            if (!existeVentas){
                JOptionPane.showMessageDialog(null, "Debe crear la tabla ventas");
                btnActualizarVenta.setEnabled(false);
                btnInsertarVenta.setEnabled(false);
                btnMostrarVentas.setEnabled(false);
                
            }else {
                btnActualizarVenta.setEnabled(true);
                btnInsertarVenta.setEnabled(true);
                btnMostrarVentas.setEnabled(true);
                
            }

        } catch (SQLException slqe) {
            slqe.getMessage();
        }
        
    }

 

