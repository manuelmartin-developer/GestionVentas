    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        
        String nombreProducto = jTextNombreProducto.getText();
        String empleado = jComboBoxCodigoEmpleado.getSelectedItem().toString();
        String unidades = jTextFieldUnidades.getText();
        
        if (nombreProducto.isEmpty()){
            JOptionPane.showMessageDialog(null, "Debe introducir un nombre para el producto");
        }else if (empleado=="Selecciona el empleado"){
            JOptionPane.showMessageDialog(null, "Debe seleccionar un empleado");
        }else if (unidades.isEmpty()){
            JOptionPane.showMessageDialog(null, "Debe introducir un número de unidades");
        }else {
            
            int codigoEmpleado = Integer.parseInt(empleado.substring(0, 1));

            int cantidad = Integer.parseInt(unidades);
            
            try {
                
                Connection conexion = this.Conexion();
                
                PreparedStatement insercion;
                insercion = conexion.prepareStatement("INSERT INTO ventas VALUES(?,?,?,?)");
                
                insercion.setInt(1,0);
                insercion.setString(2, nombreProducto);
                insercion.setInt(3, codigoEmpleado);
                insercion.setInt(4, cantidad);
                
                insercion.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "La venta se ha registrado correctamente");
                
            } catch (SQLException sqle) {
                sqle.getMessage();
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

