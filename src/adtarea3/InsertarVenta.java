    private Connection Conexion(){
        
        Connection conexion = null;
        // Conexión a la base de datos de la tienda de ropa
        String connectionUrl= "jdbc:ucanaccess://./src\\BBDD\\tiendaropa.mdb" ;
        
        try {
            conexion = DriverManager.getConnection(connectionUrl);
        } catch (SQLException sqle) {
            sqle.getMessage();
        }
        
        return conexion;
        
    }
    
    // Códigos y nombres empleados para ComboBox
    
    private void listadoEmpleados(){
        
        try {
            
            Connection conexion = this.Conexion();
            
            Statement consulta = conexion.createStatement();
            
            ResultSet resultado = consulta.executeQuery("SELECT * FROM empleados");
            
            while (resultado.next()){
                
                String codigo = Integer.toString(resultado.getInt("Codigo_empleado"));
                String nombre = resultado.getString("nombre");
                
                String elemento = codigo + " - " + nombre;
                jComboBoxCodigoEmpleado.addItem(elemento);
            }
            
        } catch (SQLException sqle) {
            sqle.getMessage();
        }
        
    }

